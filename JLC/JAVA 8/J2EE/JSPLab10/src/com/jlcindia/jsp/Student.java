package com.jlcindia.jsp;

public class Student {
	private int sid;
	private String name;
	private long phone;
	public int getSid() {
		return sid;
	}
	public String getName() {
		return name;
	}
	public long getPhone() {
		return phone;
	}
	public void setSid(int sid) {
		this.sid = sid;
	}
	public void setName(String name) {
		this.name = name;
	}
	public void setPhone(long phone) {
		this.phone = phone;
	}

}
